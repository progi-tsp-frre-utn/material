package com.frre.tsp.iteradores;

/**
 * Ejemplos de iteradores
 */
public class Iteradores {

    public static void main(String[] args) {

        //// Iteracion For
        for (int i = 0; i < 10; i++) {

        }

        //// Iteracion While
        boolean condicion2 = true;
        while (condicion2) {

        }

        //// Iteracion Do...while
        boolean condicion3 = true;
        do
        {

        } while (condicion3);
    }

}
